    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String usuario = txtUsuario.getText();
        String pass = txtPass.getText();
        
        boolean encontrar = false;
        
        for (Usuarios datos : IniciarComponentes.datosSistema) {
            
            
            if (datos.getUsuarios().equals(usuario) && datos.getPass().equals(pass)) {
               encontrar = true;
               IniciarComponentes.rol = datos.getRol();
               IniciarComponentes.usuario = datos.getUsuarios();
               break;  
            }else{
                encontrar = false;
            }
            
            
        }
        
        if (encontrar) {
            
            Principal p = new Principal();
            p.setVisible(true);
                    
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Usuario o contrase√±a incorrecto");
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


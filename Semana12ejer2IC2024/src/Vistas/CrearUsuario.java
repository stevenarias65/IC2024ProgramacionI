    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String rol = "";

            if (radioAdministrador.isSelected()) {
                rol = "1";
            } else if (radioTrabajador.isSelected()) {
                rol = "2";

            } else if (radioCajero.isSelected()) {
                rol = "3";
            }
            
            Usuarios u = new Usuarios(txtNombre.getText(),txtApellidos.getText(),txtUsuario.getText(),txtPass.getText(),rol);
            
            IniciarComponentes.datosSistema.add(u);
            JOptionPane.showMessageDialog(rootPane, "Datos ingresados");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

